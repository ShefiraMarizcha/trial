package org.example;

public class HelloGirl {
    // Metode untuk menampilkan pesan
    public void sayHello() {
        System.out.println("Hello Girl");
    }

    public static void main(String[] args) {
        // Membuat objek dari class HelloGirl
        HelloGirl hello = new HelloGirl();

        // Memanggil metode untuk menampilkan pesan
        hello.sayHello();
    }
}
